public class ArrayTest {
    public static void main(String[] args) {
        int[] ids = new int[4];
        //数组遍历
        for (int i = 0; i < ids.length; i++) {
            System.out.println(ids[i]);
        }
        String[] names = new String[4];
        //数组遍历
        for (int i = 0; i < names.length; i++) {
            System.out.println(names[i]);
        }
    }
}
