public class BinaryTest {
    public static void main(String[] args) {
        //十进制
        int num1 = 103;
        //二进制
        int num2 = 0b10;
        //八进制
        int num3 = 023;
        //十进制
        int num4 = 0x23a;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);

        int num = 0b11110101;
        System.out.println(num);
    }
}
